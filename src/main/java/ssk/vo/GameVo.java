package ssk.vo;

public class GameVo {
	private String game_id;
	private String title_ko;
	private String title_en;
	private String designer;
	private String make_year;
	private String play_min;
	private String play_max;
	public String getGame_id() {
		return game_id;
	}
	public void setGame_id(String game_id) {
		this.game_id = game_id;
	}
	public String getTitle_ko() {
		return title_ko;
	}
	public void setTitle_ko(String title_ko) {
		this.title_ko = title_ko;
	}
	public String getTitle_en() {
		return title_en;
	}
	public void setTitle_en(String title_en) {
		this.title_en = title_en;
	}
	public String getDesigner() {
		return designer;
	}
	public void setDesigner(String designer) {
		this.designer = designer;
	}
	public String getMake_year() {
		return make_year;
	}
	public void setMake_year(String make_year) {
		this.make_year = make_year;
	}
	public String getPlay_min() {
		return play_min;
	}
	public void setPlay_min(String play_min) {
		this.play_min = play_min;
	}
	public String getPlay_max() {
		return play_max;
	}
	public void setPlay_max(String play_max) {
		this.play_max = play_max;
	}
	
	
}
